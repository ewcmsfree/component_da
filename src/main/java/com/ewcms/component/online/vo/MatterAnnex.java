package com.ewcms.component.online.vo;

import java.io.Serializable;

/**
 * 事项附件
 * 
 * <ul>
 * <li>id:编号
 * <li>url:地址</li>
 * <li>legend:说明</li>
 * </ul>
 * 
 * @author 吴智俊
 */
public class MatterAnnex implements Serializable {

	private static final long serialVersionUID = -3109110630978806022L;

	private Integer id;
    private String url;
    private String name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        MatterAnnex other = (MatterAnnex) obj;
        if (id == null) {
            if (other.id != null) {
                return false;
            }
        } else if (!id.equals(other.id)) {
            return false;
        }
        return true;
    }
}
